# Spatial Reuse Access Point Design Using Wi-Fi 5 Standard

## Project Overview
This project, titled **Wi-Fi5DesignAndSim**, focuses on the design and implementation of a spatial reuse access point optimized for the Wi-Fi 5 (IEEE 802.11ac) standard. Our primary goal is to enhance network performance in high-density environments such as sports stadiums, where increased data throughput and robust signal integrity are critical.

## Objectives
- **Deep Dive into Wi-Fi 5 Capabilities**: Explore the advanced features of the Wi-Fi 5 standard that support high data rates and efficient data transmission.
- **Simulation and Design**: Utilize MATLAB for theoretical simulations and ADS (Advanced Design System) for detailed RF component design and analysis.
- **Prototype Development**: Implement a prototype based on the simulations to demonstrate the practical application of spatial reuse in dense network environments.

## Key Features
- **Wi-Fi 5 Technology**: Leverages the advantages of the 802.11ac standard to ensure faster data rates and improved user experience.
- **Spatial Reuse Techniques**: Implements strategies to reduce interference and maximize throughput in congested areas.
- **Advanced RF Design**: Includes detailed schematics and simulations of RF components tailored for optimal performance.

## Project Structure
- **MATLAB Simulations**: Contains scripts and models for system-level performance analysis.
- **ADS Designs**: Features detailed circuit designs and RF simulations essential for the hardware implementation of the access point.

